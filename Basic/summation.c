#include<stdio.h>

int main()

{
    //integer summation.
    int a = 15;
    int b = 10;
    int result = 0;// int result = a + b;
    result = a + b;
    printf("a = %d + b = %d summation = %d\n \n" , a, b, result);

    //float summation.
    float c = 35.9;
    float d = 11.11;
    float F_result = c+d;
    printf("a and b floating summation is %f + %f = %f \n", c,d,F_result);

}
