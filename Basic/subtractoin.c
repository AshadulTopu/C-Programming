#include<stdio.h>

int main()
{
    //integer value Subtraction
    int a = 50;
    int b = 29;
    int result = a - b;
    printf("%d - %d = %d\n", a,b,result);
    printf("a - b = %d\n", result);

    //floating point subtraction
    float c ,d, answer;

    c = 66.5;
    d = 23.65;
    answer = c - d;
    printf("the result is = %f\n" ,answer);
}
